function plotHandles = markersPlotTrace(Markers, MarkerNames, varargin)

colors = linspecer(size(Markers, 3));
plotHandles = [];

for n = 1 : size(Markers, 3)
    plotHandles(end+1) = ...
    plot3( ...
        squeeze(Markers(:, 1, n)), ...
        squeeze(Markers(:, 2, n)), ...
        squeeze(Markers(:, 3, n)), ...
        'Color', colors(n, :), ...
        'DisplayName', MarkerNames(n), ...
        varargin{:} ...
    );
end

end